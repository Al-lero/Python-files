import math
PI = math.pi
def areaofacircle(radius):
	areaofacircle = PI * (radius * radius)

	return f" The area of a circle is : {areaofacircle:.2f}"

print(areaofacircle(4))

print(areaofacircle(7))

print(areaofacircle(9))


